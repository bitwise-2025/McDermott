CREATE VIEW [rpy].[MDR_PO_INVOICE_PBI_V]
AS WITH InvoiceHeaderCTE AS (
    SELECT 
        o.NAME AS BU_NAME,
        i.INVOICE_ID,
        i.CREATION_DATE,
        i.DESCRIPTION,
        i.INVOICE_NUM,
        i.PARTY_ID,
        i.TASK_ID,
        i.INVOICE_DATE,
        i.INVOICE_RECEIVED_DATE,
        i.INVOICE_CURRENCY_CODE,
        i.ATTRIBUTE10,
        i.ORG_ID,
        i.AMOUNT_PAID,
        i.ATTRIBUTE15,
        i.PO_HEADER_ID,
        o.ORGANIZATION_ID,
        i.APPROVAL_STATUS,
        i.TERMS_ID,
		i.LAST_UPDATE_DATE,
        MIN(CAST(apsa.due_date AS DATE)) AS due_date
    FROM rpy.Invoice_header_ap_invoices_all_v i
    LEFT JOIN rpy.Hr_all_organization_units_v o ON i.ORG_ID = o.ORGANIZATION_ID
    LEFT JOIN rpy.Invoice_payment_schedules_v apsa 
        ON apsa.INVOICE_ID = i.INVOICE_ID AND apsa.PAYMENT_STATUS_FLAG <> 'Y'
    GROUP BY 
        o.NAME, i.INVOICE_ID, i.CREATION_DATE, i.DESCRIPTION, i.INVOICE_NUM,
        i.PARTY_ID, i.TASK_ID, i.INVOICE_DATE, i.INVOICE_RECEIVED_DATE,
        i.INVOICE_CURRENCY_CODE, i.ATTRIBUTE10, i.ORG_ID, i.AMOUNT_PAID,
        i.ATTRIBUTE15, i.PO_HEADER_ID, o.ORGANIZATION_ID,i.APPROVAL_STATUS,
        i.TERMS_ID, i.LAST_UPDATE_DATE
),

DistributionSumsCTE AS (
    SELECT 
        INVOICE_ID,
        SUM(CASE WHEN LINE_TYPE_LOOKUP_CODE NOT IN ('REC_TAX', 'AWT','PREPAY') THEN AMOUNT ELSE 0 END) AS Total_ExTax,
        SUM(CASE WHEN LINE_TYPE_LOOKUP_CODE = 'REC_TAX' THEN AMOUNT ELSE 0 END) AS Total_RecTax,
        SUM(CASE WHEN LINE_TYPE_LOOKUP_CODE = 'AWT' THEN AMOUNT ELSE 0 END) AS Total_AWT
    FROM rpy.Invoice_distributions_ap_invoice_distributions_all_v
    GROUP BY INVOICE_ID
),

RecoverableTaxCTE AS (
    SELECT 
        INVOICE_ID,
        CHARGE_APPLICABLE_TO_DIST_ID,
        SUM(ISNULL(AMOUNT, 0)) AS Recoverable_Tax
    FROM rpy.Invoice_distributions_ap_invoice_distributions_all_v
    WHERE LINE_TYPE_LOOKUP_CODE = 'REC_TAX'
    GROUP BY INVOICE_ID, CHARGE_APPLICABLE_TO_DIST_ID
),

WithholdingTaxCTE AS (
    SELECT 
        INVOICE_ID,
        AWT_RELATED_ID,
        SUM(ISNULL(AMOUNT, 0)) AS Withholding_Tax
    FROM rpy.Invoice_distributions_ap_invoice_distributions_all_v
    WHERE LINE_TYPE_LOOKUP_CODE = 'AWT'
    GROUP BY INVOICE_ID, AWT_RELATED_ID
)

SELECT 
    I_O.BU_NAME,
    I_O.INVOICE_ID,
    L.INVOICE_ID as INVOICE_ID_LINE,
    I_O.PARTY_ID,
    I_O.ORG_ID,
    I_O.ORGANIZATION_ID,
    I_O.TASK_ID,
    I_O.CREATION_DATE,
    I_O.INVOICE_RECEIVED_DATE,
    I_O.INVOICE_DATE,
    I_O.DESCRIPTION,
    I_O.ATTRIBUTE10 AS Invoice_Header_Match_Approval_Level,
    I_O.INVOICE_NUM,
    v.PAYMENT_STATUS,
    v.ACCOUNTING_STATUS,
    I_O.APPROVAL_STATUS,
    I_O.TERMS_ID,
    D.INVOICE_DISTRIBUTION_ID,
    D.DISTRIBUTION_LINE_NUMBER,
    D.LINE_TYPE_LOOKUP_CODE,
    D.CHARGE_APPLICABLE_TO_DIST_ID,
    D.AWT_RELATED_ID,
    D.PO_DISTRIBUTION_ID,
    CAST(D.ACCOUNTING_DATE AS DATE) as Accounting_Date,
    I_O.INVOICE_CURRENCY_CODE,
    I_O.AMOUNT_PAID,
    I_O.ATTRIBUTE15 AS Converted_PO,
    I_O.PO_HEADER_ID,
    I_O.due_date,
	I_O.LAST_UPDATE_DATE,
    D.AMOUNT AS Original_AMOUNT,
    Ex.CONVERSION_RATE,
    C.SEGMENT6 AS GL_PROJECT,
    --ISNULL(prj.segment1, 0) AS Project_ID,
    CASE 
        WHEN prj.segment1 IS NULL AND C.segment6 <> '00000' THEN C.segment6
        ELSE prj.segment1
    END AS Project_ID,
    prj.NAME AS PROJECT_NAME,
    prj.ATTRIBUTE5 AS Contract_No,
    prj.ATTRIBUTE4 AS Business_Line2,
    CASE 
        WHEN prj.ATTRIBUTE4 IN ('SFEH','SFWH') THEN 'SAF'
        WHEN prj.ATTRIBUTE4 IN ('OFQA','OFSA', 'OFUO') THEN 'OME'
        ELSE prj.ATTRIBUTE4
    END AS Business_Line1,
    pjt.ELEMENT_NUMBER AS TASK_NUM,
    
    --SUBSTRING(
    --    pjt.ELEMENT_NUMBER,
    --    CHARINDEX('.', pjt.ELEMENT_NUMBER) + 1,
    --    CHARINDEX('.', pjt.ELEMENT_NUMBER, CHARINDEX('.', pjt.ELEMENT_NUMBER) + 1) - CHARINDEX('.', pjt.ELEMENT_NUMBER) - 1
    --) AS JOB_NO,

    petv.EXPENDITURE_TYPE_NAME AS Exp_Type,

    CASE 
        WHEN D.LINE_TYPE_LOOKUP_CODE IN ('REC_TAX', 'AWT') THEN 0 
        ELSE D.AMOUNT 
    END AS INVOICE_DIST_AMOUNT,

    CASE 
        WHEN D.LINE_TYPE_LOOKUP_CODE IN ('REC_TAX', 'AWT','PREPAY') THEN 0
        ELSE ROUND((D.AMOUNT * I_O.AMOUNT_PAID) / NULLIF(S.Total_ExTax, 0), 2)
    END AS ALLOCATED_AMOUNT_DIST_ExTax,

    ISNULL(RT.Recoverable_Tax, 0) AS Recoverable_Tax,

    CASE 
        WHEN D.LINE_TYPE_LOOKUP_CODE <> 'REC_TAX' THEN 0
        ELSE ROUND(ISNULL(RT.Recoverable_Tax, 0) * I_O.AMOUNT_PAID / NULLIF(S.Total_RecTax, 0), 2)
    END AS ALLOCATED_AMOUNT_Rec_Tax,

    ISNULL(WT.Withholding_Tax, 0) AS Withholding_Tax,

    CASE 
        WHEN D.LINE_TYPE_LOOKUP_CODE <> 'AWT' THEN 0
        ELSE ROUND(ISNULL(WT.Withholding_Tax, 0) * I_O.AMOUNT_PAID / NULLIF(S.Total_AWT, 0), 2)
    END AS ALLOCATED_AMOUNT_WHT_Tax

FROM InvoiceHeaderCTE I_O
LEFT JOIN rpy.Invoice_status_v v ON I_O.INVOICE_ID = v.INVOICE_ID 
LEFT JOIN rpy.Invoice_line_ap_invoice_lines_all_v L ON I_O.INVOICE_ID = L.INVOICE_ID
LEFT JOIN rpy.Invoice_distributions_ap_invoice_distributions_all_v D 
    ON L.INVOICE_ID = D.INVOICE_ID AND L.LINE_NUMBER = D.INVOICE_LINE_NUMBER
LEFT JOIN DistributionSumsCTE S ON D.INVOICE_ID = S.INVOICE_ID
LEFT JOIN RecoverableTaxCTE RT ON D.INVOICE_ID = RT.INVOICE_ID AND D.INVOICE_DISTRIBUTION_ID = RT.CHARGE_APPLICABLE_TO_DIST_ID
LEFT JOIN WithholdingTaxCTE WT ON D.INVOICE_ID = WT.INVOICE_ID AND D.INVOICE_DISTRIBUTION_ID = WT.AWT_RELATED_ID
LEFT JOIN rpy.Exchange_Rates_Data_v Ex 
    ON CAST(D.ACCOUNTING_DATE AS DATE) = CAST(Ex.conversion_date AS DATE)
    AND Ex.conversion_type = 'Corporate'
    AND Ex.to_currency = 'USD'
    AND Ex.from_currency = I_O.invoice_currency_code
LEFT JOIN rpy.GL_code_combinations_v C ON C.CODE_COMBINATION_ID = D.DIST_CODE_COMBINATION_ID
LEFT JOIN rpy.Projects_Data_v prj ON prj.project_id = D.PJC_PROJECT_ID
LEFT JOIN rpy.Project_Elements_Data_v pjt ON pjt.PROJ_ELEMENT_ID = D.PJC_TASK_ID
LEFT JOIN rpy.Expenditure_Types_v petv ON petv.EXPENDITURE_TYPE_ID = D.PJC_EXPENDITURE_TYPE_ID

